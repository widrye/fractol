#include "../includes/fractol.h"

char    *ft_lowercase(char *s)
{
    size_t i;
    
    i = 0;
    while (s[i])
    {
        if (s[i] >= 65 && s[i] <= 90)
            s[i] += 32;
        i++;
    }
    s[i] = '\0';
    return (s);
}